package com.example.smproj4;

/**
 * Enumeration representing the different types of pizza crusts available.
 * Each crust corresponds to a unique style of pizza preparation.
 * 
 * Crust types:
 * - DEEP_DISH: A thick and hearty crust used for Chicago-style pizzas.
 * - PAN: A crust baked in a deep pan, often soft and fluffy.
 * - STUFFED: A crust filled with cheese or other ingredients.
 * - BROOKLYN: A thin, crispy crust typically associated with New York-style pizzas.
 * - THIN: A very thin crust with a crispy texture.
 * - HAND_TOSSED: A traditional crust prepared by tossing dough by hand.
 * 
 * This enum is used in the {@code Pizza} class to specify the crust type for each pizza.
 * 
 * @author Sinan Merchant + Varun Bondugula
 */
public enum Crust {
    DEEP_DISH, PAN, STUFFED, BROOKLYN, THIN, HAND_TOSSED
}
