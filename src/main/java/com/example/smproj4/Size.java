package com.example.smproj4;

/**
 * Enumeration representing the sizes available for pizzas.
 * 
 * The sizes determine the portion size and pricing for a pizza.
 * 
 * Sizes:
 * - {@code SMALL}: The smallest size, typically suitable for individual servings.
 * - {@code MEDIUM}: A medium size, suitable for small groups or larger individual portions.
 * - {@code LARGE}: The largest size, suitable for sharing or larger groups.
 * 
 * This enum is used in the {@code Pizza} class to define the size of a pizza.
 * 
 * @author Sinan Merchant + Varun Bondugula
 */
public enum Size {
    /**
     * Represents a small-sized pizza.
     */
    SMALL,
    
    /**
     * Represents a medium-sized pizza.
     */
    MEDIUM,
    
    /**
     * Represents a large-sized pizza.
     */
    LARGE
}

